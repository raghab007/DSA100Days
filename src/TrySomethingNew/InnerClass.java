package TrySomethingNew;

import java.util.Arrays;
import java.util.List;

// Types of Inner class
// 1. Member inner class.This inner class is used as a field in a class.Outer class instance is used to create the instance of this class
// 2. Static inner class.This class depends upon the outer class to create its objects.
// 3. Anonymous inner class.This class is used to implement the interface without writing a class that implements the interface
// 4. Local inner class.This method can be used in the methods to write logic and encapsulate.
public class InnerClass {
    public static void main(String[] args) {
        Arrays.asList(1, 2, 3);
        Arrays.asList(1, 2, 3);
    }
}
